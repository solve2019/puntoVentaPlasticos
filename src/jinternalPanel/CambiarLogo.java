    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String rutaimagen= textField.getText().trim();
        if(rutaimagen.equals("")){
            JOptionPane.showMessageDialog(null, "No ha seleccionado la imagen.", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            String ruta=new File ("").getAbsolutePath ()+"/imagenes_configurables/logo_sistema.jpg";
            System.out.println (ruta);
            File origen = new File(rutaimagen);
            File destino = new File(ruta);
            try {
                InputStream in = new FileInputStream(origen);
                OutputStream out = new FileOutputStream(destino);
                byte[] buf = new byte[1024];
                int len;

                try {
                    while ((len = in.read(buf)) > 0) {
                        out.write(buf, 0, len);
                    }
                    in.close();
                    out.close();
                    JOptionPane.showMessageDialog(null, "La imagen se modifico correctamente, para ver el cambio debe reiniciar el sistema.", "OK", JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                } catch (IOException ex) {
                    Logger.getLogger(CambiarLogo.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(null, "Error al cargar la imagen."+ex, "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (FileNotFoundException ex) {
                Logger.getLogger(CambiarLogo.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "No se encontro la ruta de la imagen."+ex, "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed


