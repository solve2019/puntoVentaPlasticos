public void vaciartabla(){
            DefaultTableModel modelo=(DefaultTableModel) jtnotascredito.getModel();         
            for (int i = 0; i < jtnotascredito.getRowCount(); i++) {
                modelo.removeRow(i);
                i-=1;
            }    
    }
    
public void cargartabla(){    
        vaciartabla();
        DefaultTableModel modelo=(DefaultTableModel) jtnotascredito.getModel();  
        conex con=new conex();   
        ResultSet rsR = null;        
        String myQuery = "select * from to_nota_credito order by id_nota desc";
               //System.out.println(""+myQuery);
               try {
                   Statement st = con.getConnection().createStatement();
                   rsR = st.executeQuery(myQuery);
                   while(rsR.next()) {                                                
                            modelo.addRow(new Object[]{rsR.getString("id_nota"),rsR.getString("fechanonta"),rsR.getString("monto_total_nota"),rsR.getString("usuario_registro")});                        
                   }
                   sorter = new TableRowSorter<TableModel>(modelo);
                   jtnotascredito.setRowSorter(sorter);
                   rsR.close(); 
                   st.close();
                   con.desconectar();
               } catch (SQLException ex) {       
                  
                   JOptionPane.showMessageDialog(null, "Error al obtener los datos: "+ex, "Error", JOptionPane.ERROR_MESSAGE);
               }     
               
              
    }    

