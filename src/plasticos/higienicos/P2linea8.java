   public void cargartabla(String categoria){    
        vaciartabla();        
        DefaultTableModel modelo=(DefaultTableModel) jTable1.getModel();  
        conex con=new conex();   
        ResultSet rsR = null;                
        String myQuery = "select idproducto,nombre_producto,precio_venta,precio_mayoreo,cantidad_mayoreo from tc_productos,tc_categorias where Idcategoria=idrcategoria and nombre='"+categoria+"' and tc_productos.estatus=1";
               //System.out.println(""+myQuery);
               try {
                   Statement st = con.getConnection().createStatement();
                   rsR = st.executeQuery(myQuery);
                   while(rsR.next()) {                              
                            modelo.addRow(new Object[]{rsR.getString("idproducto"),rsR.getString("nombre_producto"),rsR.getString("precio_venta")});                        
                   }                   
                   rsR.close(); 
                   st.close();
                   con.desconectar();
               } catch (SQLException ex) {       
                  
                   JOptionPane.showMessageDialog(null, "Error al obtener los datos: "+ex, "Error", JOptionPane.ERROR_MESSAGE);
               }     
               
              
    }     
