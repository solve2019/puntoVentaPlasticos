    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String modulo=txtmodulo.getSelectedItem().toString();
        if(modulo.equals("Selecciona..")){
            JOptionPane.showMessageDialog(null, "Debe seleccionar un modulo de la lista.","Error",JOptionPane.ERROR_MESSAGE);
        }else{
            conex con=new conex();                             
            try {  
                Statement st = con.getConnection().createStatement();            
                String myQuery = "update privilegios set "+modulo+"='1'";
                System.out.println(""+myQuery);
                st.executeUpdate(myQuery);                       
                st.close();
                con.desconectar(); 
                JOptionPane.showMessageDialog(null, "El modulo se activo correctamente.","OK",JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error al activar el modulo: "+ex,"Error",JOptionPane.ERROR_MESSAGE);
            }            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

