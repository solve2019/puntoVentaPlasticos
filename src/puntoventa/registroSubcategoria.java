   public List<catalogo> obtenerSubCatalogos(String nombre,String nombreCatalogo,Connection con) {
        List<catalogo> lista = new ArrayList<catalogo>();
        
        catalogo bean = null;
        try {
            String sql = "select * from subCatalogo where nombre=? and nombreCatalogo=? ";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1,nombre);
            ps.setString(2, nombreCatalogo);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                bean = new catalogo();
                bean.setIdCatalogo(rs.getString("idSubCatalogo"));
                bean.setNombre(rs.getString("nombre"));
                bean.setImagen(rs.getString("imagen"));
                lista.add(bean);
            }
            ps.close();
            rs.close();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en BolsasP1 metodo obtenerCatalogoPrincipal " + e.getMessage());
        }

        return lista;
    }
