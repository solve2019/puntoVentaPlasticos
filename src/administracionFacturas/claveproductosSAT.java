 public void vaciartabla(){
            DefaultTableModel modelo=(DefaultTableModel) jtproductos.getModel();         
            for (int i = 0; i < jtproductos.getRowCount(); i++) {
                modelo.removeRow(i);
                i-=1;
            }    
    }
  public void cargartabla(){    
        vaciartabla();        
        DefaultTableModel modelo=(DefaultTableModel) jtproductos.getModel();  
        conex con=new conex();   
        ResultSet rsR = null;        
        String sqlaux="";
       
        String myQuery = "select * from fact_concepto_productos";
               System.out.println(""+myQuery);
               try {
                   Statement st = con.getConnection().createStatement();
                   rsR = st.executeQuery(myQuery);
                   while(rsR.next()) {                              
                            modelo.addRow(new Object[]{rsR.getString("IdConceptos"),rsR.getString("clave_prod"),rsR.getString("descripcion"),rsR.getString("tipogiro"),rsR.getString("Unidad")});                        
                   }
                   sorter = new TableRowSorter<TableModel>(modelo);
                   jtproductos.setRowSorter(sorter);
                   rsR.close(); 
                   st.close();
                   con.desconectar();
               } catch (SQLException ex) {       
                  
                   JOptionPane.showMessageDialog(null, "Error al obtener los datos: "+ex, "Error", JOptionPane.ERROR_MESSAGE);
               }     
               
              
    }    
    
private void filtrar() {
        
         RowFilter<TableModel, Object> rf = null;
        try {
            rf = RowFilter.regexFilter("(?i)"+txtBusqueda.getText(),1,2,3,4);
        }catch (java.util.regex.PatternSyntaxException e) {
        }
        sorter.setRowFilter(rf);
    }
