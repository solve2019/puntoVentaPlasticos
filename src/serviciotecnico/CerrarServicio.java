    private void txtcobroKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcobroKeyTyped
        // TODO add your handling code here:
         if(txtcobro.getText().trim().length()>=7){
            //JOptionPane.showMessageDialog(null, "Solo acepta 15 caracteres", "Alerta", JOptionPane.ERROR_MESSAGE);
            evt.consume(); // ignorar el evento de teclado
        }else{
            if (((evt.getKeyChar() < '0') || (evt.getKeyChar() > '9')) && (evt.getKeyChar() != '.')){
               evt.consume(); // ignorar el evento de teclado
            }
        }
    }//GEN-LAST:event_txtcobroKeyTyped

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String monto=txtcobro.getText().trim();
        String correccion=txtcorreccion.getText().trim();
        if(monto.equals("")){
            txtcobro.requestFocus();
             JOptionPane.showMessageDialog(null, "No ha capturado el monto.", "OK", JOptionPane.ERROR_MESSAGE);                
        }else if(correccion.equals("")){
            JOptionPane.showMessageDialog(null, "No ha ingresado la correccion.", "OK", JOptionPane.ERROR_MESSAGE); 
        }else{
            conex cn = new conex();    
            String sql="update to_recepcion set fechaentrega=now(), monto='"+monto+"', correccion='"+correccion+"', estatus='CERRADA' where Id="+folios;
            System.out.println(sql);
            PreparedStatement pstm100;
            try {               
                pstm100 = (PreparedStatement) cn.getConnection().prepareStatement(sql);
                pstm100.executeUpdate();  
                
                pstm100.close();
                cn.desconectar();                
                ServicioTec.cargartabla();
                ticketentrega tick=new ticketentrega();
                System.out.println("folio: "+folios);
                tick.ImprimirServicioEntrega(folios);
                JOptionPane.showMessageDialog(null, "Se realizo el cierre del servicio correctamente.", "OK", JOptionPane.INFORMATION_MESSAGE);                
                dispose();
            } catch (SQLException e2) {
                    // TODO Auto-generated catch block
                    e2.printStackTrace();
            }
        
        }
    }//GEN-LAST:event_jButton1ActionPerformed

