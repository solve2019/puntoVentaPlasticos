    public void abonar(){
            String monto=txtefectivo.getText().trim();
            String formapago="";
            if(jrtarjeta.isSelected()==true){            
                formapago="TARJETA";
            }
            if(jrefectivo.isSelected()==true){            
                formapago="EFECTIVO";
            }
            boolean validador=true;
            double montook=0;
            try{
                montook = Double.parseDouble(monto);
                System.out.println(""+montook);
            } catch (NumberFormatException nfe) {
                validador=false;                
            }
            
            if(monto.equals("")){
                txtefectivo.requestFocus();
                JOptionPane.showMessageDialog(null, "El monto no debe de ir vacio.", "Alerta", JOptionPane.ERROR_MESSAGE);        
            }else if(validador==false){
                JOptionPane.showMessageDialog(null, "El monto es invalido favor de verificar.", "Alerta", JOptionPane.ERROR_MESSAGE);        
            }else{
                conex con=new conex();  
                try {
                    String myQuery = "insert into th_abonos_cliente(IdrCliente,Monto,Fecha,UsuarioMovimiento,FormaPago) values('"+idcliente+"','"+montook+"',now(),'"+accesoSistema.nombreuser+"','"+formapago+"')";
                    //System.out.println(""+myQuery);                    
                    Statement st = con.getConnection().createStatement();
                    st.executeUpdate(myQuery);
                    
                    String idabono="0";
                    myQuery="select IdAbono from th_abonos_cliente order by IdAbono desc limit 1";
                    ResultSet rs=null;
                    rs=st.executeQuery(myQuery);                    
                    while(rs.next()){
                        idabono=rs.getString("IdAbono");
                    }
                    if(formapago.equals("EFECTIVO")){
                        myQuery="update to_cierre_caja set efectivo=(efectivo+"+montook+") where id_cierre_caja ='1'";
                        st.executeUpdate(myQuery); 
                    }
                    if(formapago.equals("TARJETA")){
                        myQuery="update to_cierre_caja set tarjeta=(tarjeta+"+montook+") where id_cierre_caja ='1'";
                        st.executeUpdate(myQuery); 
                    }          
                    myQuery="update to_cierre_caja set credito=(credito+"+montook+") where id_cierre_caja ='1'";
                    st.executeUpdate(myQuery); 
                    rs.close();
                    st.close();
                    con.desconectar();                                      
                    Clientes.cargartablafolios(idcliente);
                    Clientes.vaciartablaprod();
                    Ticket imprime=new Ticket();               
                    imprime.imprimirabono(idabono+"");
                    JOptionPane.showMessageDialog(null, "El abono se registro correctamente.", "Abono registrado", JOptionPane.INFORMATION_MESSAGE);        
                    dispose();
                } catch (SQLException ex) {       
                    JOptionPane.showMessageDialog(null, "Error al agregar el abono: "+ex, "Error", JOptionPane.ERROR_MESSAGE);
                }  
            }
    }
    
    
