    public void cargar_producto(String producto_code){
                
        String codigo_barras=producto_code;
        conex con=new conex();          
        ResultSet rs = null;       
        String myQuery = "SELECT * FROM tc_productos WHERE codigo_barras='"+codigo_barras+"' or nombre_producto like '%"+codigo_barras+"%' and estatus=1";
        System.out.println(""+myQuery);
        try {  
            Statement st = con.getConnection().createStatement();
            rs = st.executeQuery(myQuery);                        
            if(rs.next()) {                             
                double iva=obteniva();
                iva=rs.getFloat("precio_venta")*iva;
                double precio=iva+rs.getFloat("precio_venta");
                funciones redondear=new funciones();
                precio=redondear.redondearDecimales(precio, 2);                
                jlprecio.setText(precio+"");                
                jlarticulo.setText(rs.getString("nombre_producto"));
            }else{
                JOptionPane.showMessageDialog(null, "No existe el producto en la base de datos favor de revisar.","Alerta",JOptionPane.ERROR_MESSAGE);
            }
            rs.close(); 
            st.close();
            con.desconectar();             
        } catch (SQLException ex) {
        }
        txtproductos.setText("");
                
    }
    
    

