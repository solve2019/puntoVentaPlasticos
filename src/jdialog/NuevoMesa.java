    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(txtcategoria.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Error falta ingresar la mesa.", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            conex con=new conex();
            try {
                Statement st = con.getConnection().createStatement();
                String myQuery = "select * from rest_mesas where Nombre='"+txtcategoria.getText().trim()+"' and esteliminacion=1";
                ResultSet rs2=st.executeQuery(myQuery);
                boolean validaexiste=false;
                while(rs2.next()) {
                    validaexiste=true;
                }
                rs2.close();
               

               

                if(validaexiste==false){
                    myQuery = "insert into rest_mesas (Nombre,esteliminacion,estatus) "
                    + "values('"+txtcategoria.getText().trim()+"',1,'LIBRE')";
                    System.out.println(""+myQuery);
                    st.executeUpdate(myQuery);                    
                    RestMesas.cargartabla();
                    JOptionPane.showMessageDialog(null, "La mesa se registro correctamente. ", "Producto registrado", JOptionPane.PLAIN_MESSAGE);
                    dispose();
                }else{
                    JOptionPane.showMessageDialog(null, "La mesa ya esta registrada", "Categoria ya existe", JOptionPane.ERROR_MESSAGE);
                }
                st.close();
                con.desconectar();
            } catch (SQLException ex) {

                JOptionPane.showMessageDialog(null, "Error al registrar la mesa: "+ex, "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

