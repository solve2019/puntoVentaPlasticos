public void cargarcategorias() {
        jccategoria.removeAllItems();
        jccategoria.addItem("Selecciona..");     
        conex con = new conex();
        ResultSet rs = null;
        String myQuery = "SELECT * from tc_categorias where estatus=1";

        try {
            Statement st = con.getConnection().createStatement();
            rs = st.executeQuery(myQuery);
            while (rs.next()) {
                jccategoria.addItem(rs.getString("Idcategoria") + "-" + rs.getString("nombre"));
            }
            
            rs = st.executeQuery("select parametro from to_parametros where clave='campo1'");
            while (rs.next()) {
                lblcampo1.setText(rs.getString("parametro"));
            }
            
            rs = st.executeQuery("select parametro from to_parametros where clave='campo2'");
            while (rs.next()) {
                lblcampo2.setText(rs.getString("parametro"));
            }
            
            rs.close();
            st.close();
            con.desconectar();
        } catch (SQLException ex) {
        }

    }
