                    myQuery = "insert into th_inventarios_registrados (id_producto,cantidad_agregada,fecha,usuario_ajusto,tipo_mov) values ('"+idprod+"','"+txtexistencia.getText().trim()+"',now(),'"+accesoSistema.nombreuser+"','INGRESO')";
