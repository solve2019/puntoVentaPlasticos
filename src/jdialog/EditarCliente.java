    public EditarCliente(java.awt.Frame parent, boolean modal,String id) {
        super(parent, modal);
        initComponents();
        this.setLocationRelativeTo(null);
        id_cliente=id;
        
        conex con=new conex();  
            try {
                String myQuery = "select * from tc_clientes where idcliente='"+id_cliente+"'";
                System.out.println(""+myQuery);
                ResultSet rsR = null; 
                Statement st = con.getConnection().createStatement();
                rsR = st.executeQuery(myQuery);
                while(rsR.next()) {                                                
                        txtnombre.setText(rsR.getString("nombre_completo"));
                        txttelefono.setText(rsR.getString("telefono"));
                        txtrfc.setText(rsR.getString("rfc"));
                        txtcorreo.setText(rsR.getString("email")); 
                        txtmunicipio.setText(rsR.getString("municipio")); 
                        txtestado.setText(rsR.getString("estado")); 
                        txtrazonsocial.setText(rsR.getString("razon_social")); 
                        txtpais.setText(rsR.getString("pais")); 
                        txtcalle.setText(rsR.getString("calle")); 
                        txtcolonia.setText(rsR.getString("colonia")); 
                        txtnoext.setText(rsR.getString("noext")); 
                        txtcp.setText(rsR.getString("cp")); 
                        txtciudad.setText(rsR.getString("ciudad")); 
                        txtnoint.setText(rsR.getString("no_interior")); 
                        txtmetodopago.setText(rsR.getString("MetodoPago")); 
                        txtnocuenta.setText(rsR.getString("noCuenta")); 
                        
                        
                }
                rsR.close();
                st.close();
                con.desconectar();                  
            } catch (SQLException ex) {       

                JOptionPane.showMessageDialog(null, "Error al obtener datos del producto: "+ex, "Error", JOptionPane.ERROR_MESSAGE);
            }  
    }

