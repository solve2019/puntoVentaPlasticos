    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(txtcategoria.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Error falta ingresar la categoria.", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            conex con=new conex();
            try {
                Statement st = con.getConnection().createStatement();
                String myQuery = "update tc_categorias set nombre='"+txtcategoria.getText().trim()+"', descripcion='"+txtdescripcion.getText().trim()+"' where Idcategoria='"+id_cate+"'";              
                System.out.println(""+myQuery);
                st.executeUpdate(myQuery);                    
                Categorias.cargartabla();
                st.close();
                con.desconectar();
                JOptionPane.showMessageDialog(null, "La categoria se modifico correctamente. ", "Producto registrado", JOptionPane.PLAIN_MESSAGE);
                dispose();               
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error al modificar la categoria: "+ex, "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

