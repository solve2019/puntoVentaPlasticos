    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String pass1=txtp1.getText().trim();
        String pass2=txtp2.getText().trim();        
        if(pass1.equals("")){
                txtp1.requestFocus();
                JOptionPane.showMessageDialog(null, "El campo de la contraseña no debe ir vacio", "Alerta", JOptionPane.ERROR_MESSAGE);        
        }else if(pass2.equals("")){
                txtp2.requestFocus();
                JOptionPane.showMessageDialog(null, "El campo de la segunda contraseña no debe ir vacio", "Alerta", JOptionPane.ERROR_MESSAGE);        
        }else{
              if(pass1.equals(pass2)){
                    System.out.println("cambio de contraseña");
                    conex con=new conex();  
                    try {
                        String myQuery = "update tc_usuarios set password=MD5('"+pass1+"') where idusuario="+accesoSistema.iduser;
                        System.out.println(""+myQuery);
                        Statement st = con.getConnection().createStatement();
                        st.executeUpdate(myQuery);
                        st.close();
                        con.desconectar();                          
                        JOptionPane.showMessageDialog(null, "La contraseña se modifico correctamente. ", "OK", JOptionPane.PLAIN_MESSAGE);
                        dispose();
                    } catch (SQLException ex) {       

                        JOptionPane.showMessageDialog(null, "Error al modificar contraseña: "+ex, "Error", JOptionPane.ERROR_MESSAGE);
                    }
                  
              }else{                  
                  txtp1.setText("");
                  txtp2.setText("");
                  txtp1.requestFocus();
                  JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden vuelva a intentarlo", "Alerta", JOptionPane.ERROR_MESSAGE);
              }  
        }
    }//GEN-LAST:event_jButton1ActionPerformed

