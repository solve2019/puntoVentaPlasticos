    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(txtnombre.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Error falta ingresar el nombre. ", "Error", JOptionPane.ERROR_MESSAGE);
        }else if(txtusuario.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Error falta ingresar el usuario. ", "Error", JOptionPane.ERROR_MESSAGE);
        }else if(txtcontra.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Error falta ingresar la contraseña. ", "Error", JOptionPane.ERROR_MESSAGE);
        }else if(cbperfil.getSelectedItem().toString().equals("Selecciona..")){
            JOptionPane.showMessageDialog(null, "Error falta seleccionar el perfil. ", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            
             conex con=new conex();  
            try {
                String myQuery = "insert into tc_usuarios (nombre,usuario,password,fecha_alta,estatus,perfil) "
                        + "values('"+txtnombre.getText().trim()+"','"+txtusuario.getText().trim()+"',MD5('"+txtcontra.getText().trim()+"'),now(),1,'"+cbperfil.getSelectedItem().toString()+"')";
                System.out.println(""+myQuery);
                Statement st = con.getConnection().createStatement();
                st.executeUpdate(myQuery);
                st.close();
                con.desconectar();  
                Usuarios.cargartabla();
                JOptionPane.showMessageDialog(null, "El usuario se registro correctamente. ", "registrado", JOptionPane.PLAIN_MESSAGE);
                dispose();
            } catch (SQLException ex) {       

                JOptionPane.showMessageDialog(null, "Error al registrar el usuario: "+ex, "Error", JOptionPane.ERROR_MESSAGE);
            } 
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

