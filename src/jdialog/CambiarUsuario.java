public void cambiaruser(){

     String user=txtusuario.getText().trim();
        String pass=txtpass.getText().trim();        
        if(user.equals("")){
            JOptionPane.showMessageDialog(null, "Falta agregar el usuario.", "Alerta", JOptionPane.ERROR_MESSAGE);
        }else if(pass.equals("")){
            JOptionPane.showMessageDialog(null, "Falta agregar la contraseña.", "Alerta", JOptionPane.ERROR_MESSAGE);        
        }else{
            accesoSistema acceso=new accesoSistema();
            boolean ingresasistema=acceso.validaAcceso(user, pass);
            if(ingresasistema){
                PantallaPrincipal.jLabel4.setText(""+accesoSistema.nombreuser);
                PantallaPrincipal.validamenu();
                dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Usuario incorrecto.", "Alerta", JOptionPane.ERROR_MESSAGE);        
            }
        }


}

